    public void updatelist() {
        dm1 = new DefaultListModel();
        for (int i = 0; i < users.size(); i++) {
            dm1.addElement(users.get(i).getUserName());
        }

       ListBox.removeAll();
       ListBox.setModel(dm1);
        CounterTxt.setText("" + users.size());
    }



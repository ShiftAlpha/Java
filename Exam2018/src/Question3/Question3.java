    private void ListBoxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListBoxMouseClicked
        String clickedName = ListBox.getSelectedValue();

        for (int i = 0; i < users.size(); i++) {
            if (clickedName.equals(users.get(i).getUserName())) {
                foundName = users.get(i).getUserName();
                foundMobile = users.get(i).getUserNum1();
                foundWork = users.get(i).getUserNum2();
                foundEmail = users.get(i).getUserEmail();

            }

        }
    }//GEN-LAST:event_ListBoxMouseClicked

